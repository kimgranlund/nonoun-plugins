#!/usr/bin/env python3
"""scaffold.py — stand up a fresh debug project + a dev-factory instance from a brief.

    python3 debug/bin/scaffold.py <name> [--brief solitaire] [--force]

Creates debug/runs/<name>/ (a fresh git project), a dev-factory instance under it, a dev-factory.env for
run.sh, project-local .claude/settings.json wiring the plugins from LOCAL source, and seeds the brief as a
PROMPT intake ticket (dogfooding the prompt-mode ticket — the cold-start planner triages it next).

Deterministic, no model. Stdlib only.
"""
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
import _common as C   # noqa: E402


def _settings_json():
    """Project-local .claude/settings.json: enable the dev-factory plugins from the LOCAL working tree.

    A LOCAL marketplace is a bare STRING path (absolute or ./-relative) to the dir holding
    .claude-plugin/marketplace.json — NOT an object (per the Claude Code docs, "Add from local paths"). The
    marketplace key here ("dev-factory") is independent of the name inside that marketplace.json. The loop does
    NOT depend on the plugin *loader* — the dev-server resolves the kernel bins + kit by PATH (DEV_KERNEL_BIN /
    DEV_FACTORY_KIT) and dispatched workers get them via --add-dir, so the factory runs even if this install
    fails; the install just adds the nicer UX (slash commands, auto-loaded skills)."""
    return {
        "extraKnownMarketplaces": {
            "dev-factory": C.DEVFACTORY   # bare absolute path to the local marketplace dir
        },
        "enabledPlugins": {
            "dev-kernel@dev-factory": True,
            "dev-kit-corpus@dev-factory": True,
            "dev-kit-app@dev-factory": True,
        },
    }


def _env_file(name):
    inst = C.instance_dir(name)
    return "\n".join([
        "# dev-factory.env — operator config for run.sh (generated by debug/bin/scaffold.py).",
        f"DEV_FACTORY_DIR={inst}",
        f"DEV_KERNEL_BIN={C.KERNEL_BIN}",
        f"DEV_FACTORY_KIT={C.KIT_CORPUS}",
        "",
        "# Walk (autonomous) with hard caps — the ralph loop wraps these with its own outer ceiling.",
        "DEV_FACTORY_HEARTBEAT=1",
        "DEV_FACTORY_MAX_DISPATCHES=24",
        "DEV_FACTORY_DEADLINE_S=3600",
        "DEV_FACTORY_TOKEN_CEILING=4000000",
        "DEV_FACTORY_TIER=2",
        "DEV_FACTORY_CONCURRENCY=2",
        "DEV_FACTORY_PERIOD=30",
        "DEV_FACTORY_INPUT_PERIOD=5",
        "PORT=8731",
        "",
    ])


def scaffold(name, brief_arg="solitaire", force=False):
    api = C._import_api()
    proj = C.project_dir(name)
    inst = C.instance_dir(name)
    if os.path.exists(proj) and not force:
        raise SystemExit(f"{proj} already exists — pass --force to reseed, or pick a new name.")
    os.makedirs(proj, exist_ok=True)

    C.banner(f"scaffolding debug project '{name}' at {os.path.relpath(proj, C.REPO)}")
    # a fresh git repo so dispatched workers get hermetic `git worktree` isolation (else a plain-dir fallback)
    if not os.path.isdir(os.path.join(proj, ".git")):
        C.sh(["git", "init", "-q"], cwd=proj, check=False)
        # inline identity so a HEAD exists even where git has no global user config (CI) — needed for
        # `git worktree add` hermetic worker isolation; a failure just falls back to a plain-dir worktree
        C.sh(["git", "-c", "user.email=debug@dev-factory", "-c", "user.name=dev-factory debug",
              "commit", "-q", "--allow-empty", "-m", "debug project seed"], cwd=proj, check=False)

    # the instance: scaffold the substrate + coordination dirs + empty lattice (idempotent — same as server boot)
    api.init_instance(inst)
    print(f"  instance: {os.path.relpath(inst, C.REPO)}")

    # project-local plugin wiring (LOCAL source) + the run.sh env
    C.write_json(os.path.join(proj, ".claude", "settings.json"), _settings_json())
    open(os.path.join(proj, "dev-factory.env"), "w", encoding="utf-8").write(_env_file(name))
    print("  wrote .claude/settings.json (local source) + dev-factory.env")

    # seed the brief as a PROMPT intake ticket — the cold-start planner triages it (prompt = triaged)
    brief_path, brief_text = C.load_brief(brief_arg)
    title = brief_text.strip().splitlines()[0].lstrip("# ").strip()[:80] or name
    t = api.create_ticket(inst, "prompt", title, body=brief_text, created_by="operator")
    print(f"  seeded PROMPT ticket {t['id']} from {os.path.relpath(brief_path, C.REPO)}")

    C.banner("scaffold complete")
    print(f"  next:  python3 debug/bin/coldstart.py {name}        # brief -> spec + hydrated lattice + build tickets")
    print(f"  then:  python3 debug/bin/ralph.py {name}            # the bounded build loop (DEBUG_RALPH_LIVE=1 for real workers)")
    print(f"  watch: cd {os.path.relpath(proj, C.REPO)} && ../../dev-factory/dev-server/run.sh   # the dashboard at http://127.0.0.1:8731")
    return {"project": proj, "instance": inst, "prompt_ticket": t["id"]}


def main(argv):
    if not argv or argv[0] in ("-h", "--help"):
        print(__doc__)
        return 0
    name = argv[0]
    brief = argv[argv.index("--brief") + 1] if "--brief" in argv else "solitaire"
    force = "--force" in argv
    scaffold(name, brief, force)
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
